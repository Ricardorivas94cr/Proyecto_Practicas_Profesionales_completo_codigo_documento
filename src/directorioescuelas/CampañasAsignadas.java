    private void btneliminar_asigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminar_asigActionPerformed
        int fila =
        tablacampana_asignadas.getSelectedRow();

        if (fila >= 0) {
            int idCampaña =
            Integer.parseInt(tablacampana_asignadas.getValueAt(fila,0).toString());
            int idEscuela =
            Integer.parseInt(tablacampana_asignadas.getValueAt(fila,1).toString());

            int confirmacion =
            JOptionPane.showConfirmDialog(null, "¿Deseas eliminar esta Asignación", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (confirmacion == JOptionPane.YES_OPTION){
                IntercampañasDAO dao = new
                IntercampañasDAO();

                dao.eliminarAsignacion(idCampaña, idEscuela);

                JOptionPane.showMessageDialog(null,"Asignación eliminada");
                cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null,"Selecciona una fila");
            }
        }
    }//GEN-LAST:event_btneliminar_asigActionPerformed

