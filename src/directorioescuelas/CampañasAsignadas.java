    private void btnasigcam_imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnasigcam_imprimirActionPerformed
        
      try {
        
         boolean completo =
         tablacampana_asignadas.print(
         JTable.PrintMode.FIT_WIDTH,
         new MessageFormat("Reporte de Campañas Asignadas"),
         new MessageFormat("Página {0}")
                 
         );
         
         if (completo){
         JOptionPane.showMessageDialog(null, "Impresión Completada");
         } else {
         JOptionPane.showMessageDialog(null, "Impresión Cancelada");
         }
        
        } catch (PrinterException e){
         JOptionPane.showMessageDialog(null, "Error al Imprimir" + e.getMessage());
        }  
    }//GEN-LAST:event_btnasigcam_imprimirActionPerformed

