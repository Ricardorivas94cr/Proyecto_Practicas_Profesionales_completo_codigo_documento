    private void btnasigev_imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnasigev_imprimirActionPerformed
        
     try {
        
         boolean completo =
         tablaeventos_asignados.print(
         JTable.PrintMode.FIT_WIDTH,
         new MessageFormat("Reporte de Eventos Asignados"),
         new MessageFormat("Página {0}")
                 
         );
         
         if (completo){
         JOptionPane.showMessageDialog(null, "Impresión Completada");
         } else {
         JOptionPane.showMessageDialog(null, "Impresión Cancelada");
         }
        
        } catch (PrinterException e){
         JOptionPane.showMessageDialog(null, "Error al Imprimir" + e.getMessage());
        }   
    }//GEN-LAST:event_btnasigev_imprimirActionPerformed

