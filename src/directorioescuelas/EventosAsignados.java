    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int fila =
        tablaeventos_asignados.getSelectedRow();
        
        if (fila >= 0) {
            int idEvento =
            Integer.parseInt(tablaeventos_asignados.getValueAt(fila,0).toString());
            int idEscuela =
             Integer.parseInt(tablaeventos_asignados.getValueAt(fila,1).toString());
            
            int confirmacion =
            JOptionPane.showConfirmDialog(null, "¿Deseas eliminar esta Asignación", "Confirmar", JOptionPane.YES_NO_OPTION);
            
            if (confirmacion == JOptionPane.YES_OPTION){
            InterEventosDAO dao = new
            InterEventosDAO();
            
            dao.eliminarAsignacion(idEvento, idEscuela);
            
            JOptionPane.showMessageDialog(null,"Asignación eliminada");
            cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null,"Selecciona una fila");
            }
        } 
    }//GEN-LAST:event_btneliminarActionPerformed

